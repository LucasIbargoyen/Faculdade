paralelepipedo = int(input("Digite a medida do paralelepipedo: "))
area_base = int(input("Digite o valor da área base: "))
altura = int(input("Digite o valor da altura: "))

formula = area_base * altura

print(f"O volume de um paralelepipedo de {paralelepipedo} mm considerando a área base de {area_base} e a altura de {altura} mm é de {formula} mm")