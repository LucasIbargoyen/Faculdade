retangulo = int(input("Digite a medida do retângulo (em mm): "))
base = int(input("Digite o valor da base do retângulo(em mm): "))
altura = int(input("Digite o valor da altura do retângulo (em mm): "))

calculo = base * altura

print(f"A área de um retângulo de {retangulo} mm é de {calculo} mm")